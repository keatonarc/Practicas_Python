#Rebound lista se requiere eliminar duplicados y ordenar en orden ascendente
mi_lista = [5, 20, 15, 20, 25, 50, 20, 5, 18, 15]
#set para eliminar duples
lista_sin_duplicados = list(set(mi_lista))
#ordenar en ascendente
lista_sin_duplicados.sort()
#impresion de la lista en ascendente
print(lista_sin_duplicados)
